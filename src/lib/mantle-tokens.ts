// Get all unique CoinGecko IDs for batch price fetching
export function getCoinGeckoIds(): string[] {
  return MANTLE_TOKENS
    .filter(t => t.coingeckoId)
    .map(t => t.coingeckoId as string);
}

// Get token by symbol
export function getTokenBySymbol(symbol: string): MantleToken | undefined {
  return MANTLE_TOKENS.find(t => t.symbol === symbol || t.id === symbol);
}

// Get tokens by category
export function getTokensByCategory(category: MantleToken["category"]): MantleToken[] {
  return MANTLE_TOKENS.filter(t => t.category === category);
}

// Format contract address for display
export function formatAddress(address: string): string {
  return `${address.slice(0, 6)}...${address.slice(-4)}`;
}

// Get Mantle explorer URL for token
export function getTokenExplorerUrl(contractAddress: string): string {
  return `https://explorer.mantle.xyz/token/${contractAddress}`;
}

// Get token count by category
export function getTokenCountByCategory(): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const token of MANTLE_TOKENS) {
    counts[token.category] = (counts[token.category] || 0) + 1;
  }
  return counts;
}
